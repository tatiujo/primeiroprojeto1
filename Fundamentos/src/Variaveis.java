
public class Variaveis {

	public static void main(String[] args) {
		//variável inteiro
		int inteiro = 15;
		System.out.println(inteiro);
		inteiro = 10;
		System.out.println(inteiro);
		
		//variável texto
		String texto;
		texto = "Olá";
		System.out.println("texto");
		
		//variável double
		double preco;
		preco = 24.50;
		System.out.println(preco);
	}
}
